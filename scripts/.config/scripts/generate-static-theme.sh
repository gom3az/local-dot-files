-- Static theme: $THEME_NAME
local M = {}

M.colors = {
    rosewater = "$TEXT",
    flamingo = "$TEXT",
    pink = "$COLOR5",
    mauve = "$MAUVE",
    red = "$RED",
    maroon = "$MAROON",
    peach = "$PEACH",
    yellow = "$YELLOW",
    green = "$GREEN",
    teal = "$TEAL",
    sky = "$SKY",
    sapphire = "$SAPPHIRE",
    blue = "$BLUE",
    lavender = "$LAVENDER",
    text = "$FG",
    subtext1 = "$SUBTEXT1",
    subtext0 = "$SUBTEXT0",
    overlay2 = "$OVERLAY2",
    overlay1 = "$OVERLAY1",
    overlay0 = "$OVERLAY0",
