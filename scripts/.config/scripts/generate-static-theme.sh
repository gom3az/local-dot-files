#!/usr/bin/env bash
# =============================================================================
# generate-static-theme.sh - Build a static theme from a color definition JSON
# =============================================================================
# Usage: generate-static-theme.sh <theme-definition.json> <output-dir>
#
# The JSON should have:
#   {
#     "name": "theme-name",
#     "wallpaper": "path or description",
#     "colors": {
#       "background": "#...", "foreground": "#...", "cursor": "#...",
#       "color0": "#..." through "color15": "#...",
#       "crust": "#...", "mantle": "#...", "base": "#...",
#       "surface0": "#...", "surface1": "#...", "surface2": "#...",
#       "overlay0": "#...", "overlay1": "#...", "overlay2": "#...",
#       "subtext0": "#...", "subtext1": "#...", "text": "#..."
#     }
#   }
# Tokens are read from ~/.config/themes/tokens.json
# =============================================================================

set -euo pipefail

if [[ $# -lt 2 ]]; then
    echo "Usage: $0 <theme-definition.json> <output-dir>"
    exit 1
fi

DEFINITION="$1"
OUTPUT_DIR="$2"
TOKENS_FILE="$HOME/.config/themes/tokens.json"

if [[ ! -f "$DEFINITION" ]]; then
    echo "Error: $DEFINITION not found"
    exit 1
fi

mkdir -p "$OUTPUT_DIR"

# Export all colors as shell variables
eval "$(python3 -c "
import json
with open('$DEFINITION') as f:
    d = json.load(f)

out = []
for k, v in d.get('colors', d).items():
    k = k.upper().replace('-', '_')
    v = v.strip()
    if v and not v.startswith('#'):
        v = '#' + v
    out.append(f'{k}=\"{v}\"')
print('; '.join(out))
")"

# Derive aliases
BG="${BACKGROUND:-$COLOR0}"
FG="${FOREGROUND:-$COLOR7}"
CURSOR="${CURSOR:-$MAUVE}"
RED="${RED:-$COLOR1}"
GREEN="${GREEN:-$COLOR2}"
YELLOW="${YELLOW:-$COLOR3}"
BLUE="${BLUE:-$COLOR4}"
MAUVE="${MAUVE:-$COLOR5}"
TEAL="${TEAL:-$COLOR6}"
PINK="${PINK:-$COLOR5}"
MAROON="${MAROON:-$COLOR1}"
PEACH="${PEACH:-$COLOR3}"
SAPPHIRE="${SAPPHIRE:-$COLOR4}"
SKY="${SKY:-$COLOR6}"
LAVENDER="${LAVENDER:-$COLOR6}"
TEXT="${TEXT:-$FG}"
SURFACE0="${SURFACE0:-$COLOR0}"
SURFACE1="${SURFACE1:-$COLOR8}"
SURFACE2="${SURFACE2:-$COLOR7}"
CRUST="${CRUST:-$COLOR0}"
MANTLE="${MANTLE:-$COLOR0}"
BASE="${BASE:-$COLOR0}"
OVERLAY0="${OVERLAY0:-$COLOR7}"
OVERLAY1="${OVERLAY1:-$COLOR7}"
OVERLAY2="${OVERLAY2:-$COLOR7}"
SUBTEXT0="${SUBTEXT0:-$COLOR7}"
SUBTEXT1="${SUBTEXT1:-$COLOR7}"

# Default any empty color hexes
for var in COLOR0 COLOR1 COLOR2 COLOR3 COLOR4 COLOR5 COLOR6 COLOR7 \
           COLOR8 COLOR9 COLOR10 COLOR11 COLOR12 COLOR13 COLOR14 COLOR15 \
           BG FG CURSOR RED GREEN YELLOW BLUE MAUVE TEAL PINK MAROON PEACH \
           SAPPHIRE SKY LAVENDER TEXT CRUST MANTLE BASE SURFACE0 SURFACE1 \
           SURFACE2 OVERLAY0 OVERLAY1 OVERLAY2 SUBTEXT0 SUBTEXT1; do
    val="${!var:-}"
    if [[ -z "$val" || "$val" == '#' ]]; then
        eval "$var=\"#000000\""
    fi
done

# Load tokens
if [[ -f "$TOKENS_FILE" ]]; then
    FONT_SANS=$(python3 -c "import json; d=json.load(open('$TOKENS_FILE')); print(d.get('fonts', {}).get('sans', 'NotoSans Nerd Font'))")
    FONT_SIZE=$(python3 -c "import json; d=json.load(open('$TOKENS_FILE')); print(d.get('fonts', {}).get('size', '12px'))")
    FONT_SIZE_LARGE=$(python3 -c "import json; d=json.load(open('$TOKENS_FILE')); print(d.get('fonts', {}).get('size_large', '15px'))")
    W_RADIUS=$(python3 -c "import json; d=json.load(open('$TOKENS_FILE')); print(str(d.get('radii', {}).get('window', 0)))")
    S_TIGHT=$(python3 -c "import json; d=json.load(open('$TOKENS_FILE')); print(d.get('spacing', {}).get('tight', '4px'))")
else
    FONT_SANS="NotoSans Nerd Font"
    FONT_SIZE="12px"
    FONT_SIZE_LARGE="15px"
    W_RADIUS=0
    S_TIGHT="4px"
fi

THEME_NAME=$(python3 -c "import json; print(json.load(open('$DEFINITION')).get('name', 'unnamed'))" 2>/dev/null || echo "unnamed")
WP_NAME=$(python3 -c "import json; print(json.load(open('$DEFINITION')).get('wallpaper', 'static'))" 2>/dev/null || echo "static")

# === Generate theme.css ===
cat > "$OUTPUT_DIR/theme.css" << CSSEOF
