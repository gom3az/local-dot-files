-- Static theme: $THEME_NAME

local theme = {}

theme.colors = {
    background = "${BG#\#}",
    foreground = "${FG#\#}",
    cursor = "${CURSOR#\#}",
    crust = "${CRUST#\#}",
    mantle = "${MANTLE#\#}",
    base = "${BASE#\#}",
    surface0 = "${SURFACE0#\#}",
    surface1 = "${SURFACE1#\#}",
    surface2 = "${SURFACE2#\#}",
    overlay0 = "${OVERLAY0#\#}",
    overlay1 = "${OVERLAY1#\#}",
    overlay2 = "${OVERLAY2#\#}",
    subtext0 = "${SUBTEXT0#\#}",
    subtext1 = "${SUBTEXT1#\#}",
    text = "${FG#\#}",
    red = "${RED#\#}",
    maroon = "${MAROON#\#}",
    peach = "${PEACH#\#}",
    yellow = "${YELLOW#\#}",
    green = "${GREEN#\#}",
    teal = "${TEAL#\#}",
    sky = "${SKY#\#}",
    sapphire = "${SAPPHIRE#\#}",
    blue = "${BLUE#\#}",
    mauve = "${MAUVE#\#}",
    pink = "${PINK#\#}",
    lavender = "${LAVENDER#\#}",
}

function theme.rgba(hex, alpha)
    alpha = alpha or "ee"
    return "rgba(" .. hex .. alpha .. ")"
end

return theme
LUAEOF

